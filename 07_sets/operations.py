set_a = {1, 2, 3, 4}
set_b = {3, 4, 5, 6}

# Example 1: Union and Intersection
print(f"Union (all unique): {set_a | set_b}")
print(f"Intersection (common): {set_a & set_b}")

# Example 2: Difference and Symmetric Difference
print(f"Difference (A but not B): {set_a - set_b}")
print(f"Symmetric Difference (Not in both): {set_a ^ set_b}")

