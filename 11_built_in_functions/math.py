# Example 1: Math functions
nums = [1, 2, 3, 4, 5]

print(f"Absolute of -10: {abs(-10)}")
print(f"Round 3.14159: {round(3.14159, 2)}")
print(f"Min: {min(nums)}, Max: {max(nums)}")
print(f"Sum: {sum(nums)}")

# Example 2: Power and Divmod
print(f"2 to the power 3: {pow(2, 3)}")
print(f"10 divided by 3 (q, r): {divmod(10, 3)}")

