# Example 1: Break
print("Break at 5:")
for i in range(1, 10):
    if i == 5:
        break
    print(i)

# Example 2: Continue
print("Skip even numbers:")
for i in range(1, 10):
    if i % 2 == 0:
        continue
    print(i)

